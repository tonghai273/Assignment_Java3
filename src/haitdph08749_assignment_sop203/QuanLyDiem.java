    public void fillTable() {
        String [] tenn={"Họ và tên","Mã SV","Tiếng Anh","Tin học","Giáo dục TC","Trung bình"};
        model = new DefaultTableModel(tenn,0);
        for(int i=0;i<list.size();i++){
            String hoten = list.get(i).name;
            String ma = list.get(i).masv;
            double TA = list.get(i).anh;
            double tin = list.get(i).tin;
            double gd = list.get(i).gdtc;
            double tb = list.get(i).avg;
            Object [] ob={hoten,ma,TA,tin,gd,tb};
            model.addRow(ob);
        }
        tbHienthi.setModel(model);
    }
