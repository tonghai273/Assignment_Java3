    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if(txtMaTimKiem.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Chưa nhập mã số sinh viên để tìm kiếm");
            txtMaTimKiem.requestFocus();
        }
        try{
            String sql = "SELECT * FROM STUDENTS WHERE MASV=?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, txtMaTimKiem.getText());
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                lblHovaten.setText(rs.getString("HOTEN"));
                txtMaSV.setText(rs.getString("MASV"));
            }
        }
        catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

