    public void loadDataToTable(){
        try{
            model.setRowCount(0);
        String sql = "SELECT TOP 3 STUDENTS.MASV,HOTEN,TIENGANH,TINHOC,GDTC,(TIENGANH+TINHOC+GDTC)/3 AS TBM FROM STUDENTS JOIN GRADE\n"
                    + " ON STUDENTS.MASV = GRADE.MASV ORDER BY TBM DESC";
        Statement s = con.createStatement();
        ResultSet rs = s.executeQuery(sql);
        while(rs.next()){
            Vector vec = new Vector();
            vec.add(rs.getString("MASV"));
            vec.add(rs.getString("HOTEN"));
            vec.add(Double.parseDouble(rs.getString("TIENGANH")));
            vec.add(Double.parseDouble(rs.getString("TINHOC")));
            vec.add(Double.parseDouble(rs.getString("GDTC")));
            vec.add(Double.parseDouble(rs.getString("TBM")));
            model.addRow(vec);
        }
        tbHienthi.setModel(model);
        }
        catch(Exception e){
            System.out.println(e);
        }
    }
    
